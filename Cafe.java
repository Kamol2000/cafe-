package by.cafe.models;

public class Cafe {
    private int date_time;
    private int rating;


    public Cafe() {
    }


    public int getDate_time() {
        return date_time;
    }

    public void setDate_time(int date_time) {
        this.date_time = date_time;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }



}
